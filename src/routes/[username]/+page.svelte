<script lang="ts">
    import type { PageData } from './$types';
    import {user, userData} from '$lib/firebase'
    import UserLink from '$lib/components/UserLink.svelte';
    import {page} from '$app/stores'
    export let data: PageData;

    let copied = false
    let copyTimer:NodeJS.Timeout
    const copyLink = ()=>{
        navigator.clipboard.writeText($page.url.href).then(
            ()=>{
                copied = true
                clearTimeout(copyTimer)
                copyTimer = setTimeout(
                    ()=>copied=false,5000
                )
            }
        ).catch(
            ()=>copied=false
        )

    }
</script>

<svelte:head>
    <title>@{data.username} Links</title>
    <meta name='description' content={data.bio} />
</svelte:head>
<main class="flex-col flex gap-3 items-center mx-auto mb-20">
    <h1 class="text-primary text-2xl my-6">@<span class="text-4xl">{data.username}</span></h1>
    <img 
        class="avatar rounded-xl w-80" 
        src={data.photoURL??'/user.png'} 
        alt={`${data?.username}`}
    />
    <p>{data.bio??"no  bio yet.."}</p>
    <ul class="list-none flex flex-col gap-4 w-full mt-5">
        {#each  data.links as item}
            <li class="list-item">
                <UserLink {...item} />
            </li>
        {/each}
    </ul>
    {#if ($user&&data.username===$userData?.username)}
    <div class="flex gap-4 mt-5">
        <a href={`/${$userData?.username}/edit`} class="btn flex-1 btn-outline flex gap-2">
             <svg xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" stroke-width="1.5" stroke="currentColor" class="size-6">
                <path stroke-linecap="round" stroke-linejoin="round" d="m16.862 4.487 1.687-1.688a1.875 1.875 0 1 1 2.652 2.652L10.582 16.07a4.5 4.5 0 0 1-1.897 1.13L6 18l.8-2.685a4.5 4.5 0 0 1 1.13-1.897l8.932-8.931Zm0 0L19.5 7.125M18 14v4.75A2.25 2.25 0 0 1 15.75 21H5.25A2.25 2.25 0 0 1 3 18.75V8.25A2.25 2.25 0 0 1 5.25 6H10" />
              </svg>
              
            <span class="flex-1">Edit Your Profile</span>
        </a>
        <button on:click={copyLink} class="btn btn-neutral flex gap-2 "> 
            {#if !copied}
                <svg xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" stroke-width="1.5" stroke="currentColor" class="size-6">
                    <path stroke-linecap="round" stroke-linejoin="round" d="M15.666 3.888A2.25 2.25 0 0 0 13.5 2.25h-3c-1.03 0-1.9.693-2.166 1.638m7.332 0c.055.194.084.4.084.612v0a.75.75 0 0 1-.75.75H9a.75.75 0 0 1-.75-.75v0c0-.212.03-.418.084-.612m7.332 0c.646.049 1.288.11 1.927.184 1.1.128 1.907 1.077 1.907 2.185V19.5a2.25 2.25 0 0 1-2.25 2.25H6.75A2.25 2.25 0 0 1 4.5 19.5V6.257c0-1.108.806-2.057 1.907-2.185a48.208 48.208 0 0 1 1.927-.184" />
                </svg>
            {:else}
                <svg xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" stroke-width="1.5" stroke="currentColor" class="size-6">
                    <path stroke-linecap="round" stroke-linejoin="round" d="M11.35 3.836c-.065.21-.1.433-.1.664 0 .414.336.75.75.75h4.5a.75.75 0 0 0 .75-.75 2.25 2.25 0 0 0-.1-.664m-5.8 0A2.251 2.251 0 0 1 13.5 2.25H15c1.012 0 1.867.668 2.15 1.586m-5.8 0c-.376.023-.75.05-1.124.08C9.095 4.01 8.25 4.973 8.25 6.108V8.25m8.9-4.414c.376.023.75.05 1.124.08 1.131.094 1.976 1.057 1.976 2.192V16.5A2.25 2.25 0 0 1 18 18.75h-2.25m-7.5-10.5H4.875c-.621 0-1.125.504-1.125 1.125v11.25c0 .621.504 1.125 1.125 1.125h9.75c.621 0 1.125-.504 1.125-1.125V18.75m-7.5-10.5h6.375c.621 0 1.125.504 1.125 1.125v9.375m-8.25-3 1.5 1.5 3-3.75" />
                </svg>
            {/if}
            
            {copied?"Copied":"Copy Link"}
        </button>
    </div>
    {/if}
</main>